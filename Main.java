class Main {

  public static void main(String[] args) {
      System.out.println("Testing the for loop feature");
    for(int i=0; i < 5; ++i){ //For loop feature 
      System.out.println(i);
    }
    double test = 0.13564534656547657; //Double type feature 
    System.out.println("This is the double type: ");
    System.out.println(test); 
    int x = 5;
    int y = 4;
    int z;
    z = x % y; //modulo feature 
    System.out.println("Result of 5 mod 4: ");
    System.out.println(z);
  }
}
